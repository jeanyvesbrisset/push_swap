#include "libft.h"

int	ft_is_nb(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!ft_isdigit(str[i]) && str[i] != '-' && str[i] != '+')
			return (0);
		if ((str[i] == '-' || str[i] == '+') && (i != 0 || str[i + 1] == '\0'))
			return (0);
		i++;
	}
	return (1);
}
